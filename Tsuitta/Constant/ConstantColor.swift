import Foundation
import UIKit

class ConstantColor {
    
    static func black() -> UIColor {
        
        return UIColor.hex("282828", alpha: 1.0)
        
    }
    
    static func gray() -> UIColor {
        
        return UIColor.hex("9c9c9c", alpha: 1.0)
        
    }
    
}

